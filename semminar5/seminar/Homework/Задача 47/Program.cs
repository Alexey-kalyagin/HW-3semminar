// Задайте двумерный массив размером m×n, заполненный случайными вещественными числами.
// m = 3, n = 4.
// 0,5 7 -2 -0,2
// 1 -3,3 8 -9,9
// 8 7,8 -7,1 9

int [,] number =    {{0,5,7,-2},
                     {1,-3,3,8},
                     {8,7,8,-7}};
int m = 3;
int n = 4;

for (int i = 0; i < m; i++)
{
    for (int j = 0; j < n; j++)
    {
        Console.Write($"{number[i,j]} {" "}");
    } 
    Console.WriteLine();
} 